function theaterImage(imageLink) {
    var image = document.getElementById("theater-image");
    image.src = imageLink;
    var theater = document.getElementById("theater");
    theater.style.display = "block";
}

function closeTheater() {
    var theater = document.getElementById("theater");
    theater.style.display = "none";
    var image = document.getElementById("theater-image");
    //image.src = "about:blank";
    image.removeAttribute('src');
}

function revealCode(modName) {
    let notepad_content = document.getElementById("notepad-content");
    let currentMod = mods.find(mod => mod.name === modName)
    notepad_content.innerText = currentMod.name + "\nType: " + currentMod.type + "\n" + currentMod.code;
    //document.getElementById("notepad-header").innerHTML = "<b>" + currentMod.name + "</b>";
}

function showMods() {
    let gallery = document.getElementById("gallery");
    for (var i = 0; i < mods.length; i++) {
        let galleryItem = document.createElement("LI");
        galleryItem.setAttribute('class', 'gallery-item');
        let galleryItemHeader = document.createElement("SPAN");
        galleryItemHeader.setAttribute('class', 'gallery-item-header');
        galleryItemHeader.innerText = mods[i].game + ": " + mods[i].name;
        galleryItem.appendChild(galleryItemHeader);
        let galleryItemImageLink = document.createElement("A");
        galleryItemImageLink.setAttribute('href', '#!');
        galleryItemImageLink.setAttribute("onclick", "theaterImage('" + mods[i].img + "')");
        let galleryItemImageContainer = document.createElement("DIV");
        galleryItemImageContainer.setAttribute("class", "gallery-item-image-container");
        let galleryItemImage = document.createElement("IMG");
        galleryItemImage.setAttribute("src", mods[i].img);
        galleryItemImageContainer.appendChild(galleryItemImage);
        galleryItemImageLink.appendChild(galleryItemImageContainer);
        galleryItem.appendChild(galleryItemImageLink);
        let galleryItemButton = document.createElement("A");
        galleryItemButton.setAttribute("class", "gallery-item-button");
        galleryItemButton.setAttribute("href", "#");
        galleryItemButton.setAttribute("onclick", "revealCode('" + mods[i].name + "')");
        galleryItemButton.innerText = "Open Code";
        galleryItem.appendChild(galleryItemButton);
        gallery.appendChild(galleryItem);
    }
}